package com.ger.reclamapoa;

public class CAR {
	public double lat;
	public double lon;
	public String name;
	public String email;
	public String subject;

	
	public CAR(double lat, double lon, String name, String email,
			String subject) {
		super();
		this.lat = lat;
		this.lon = lon;
		this.name = name;
		this.email = email;
		this.subject = subject;
	}
	
	
	
}
